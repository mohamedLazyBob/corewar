int		ft_isprime(int nb)
{
	int i;
	int compt;

	i = 1;
	compt = 1;
	while (i <= nb / 2)
	{
		if (nb % i == 0)
		{
			compt++;
			if (compt > 2)
				return (0);
		}
		i++;
	}
	if (compt == 2)
		return (1);
	else
		return (0);
}
