#include <libft.h>

int		ft_strncmp(const char *s1, const char *s2, size_t size)
{
	while (size--)
	{
		if (*s1 != *s2 || !*s1 || !*s2)
			return ((unsigned char)*s1 - (unsigned char)*s2);
		s1++;
		s2++;
	}
	return (0);
}
