#include <ft_printf.h>

char	*omit_zero(char **nbr)
{
	char	*ret;

	if ((*nbr)[0] == '0')
		ret = ft_strdup(*nbr + 1);
	else
		return (*nbr);
	ft_strdel(nbr);
	return (ret);
}
